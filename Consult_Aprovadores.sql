SELECT DISTINCT
    CASE Z01.EMPRESA
        WHEN 'COMPASA DO BRASIL' THEN '01'
        WHEN 'CLL - COMPASA LOGISTICA LTDA' THEN '02'
        WHEN 'CONSORCIO COMPASA - GAISSLER' THEN '03'
        WHEN 'COMPASA - BR-INFRA | RDC4' THEN '04'
        WHEN 'COMPASA - BR-INFRA | RDC6' THEN '05'
        WHEN 'ATS ASFALTOS TECNOLOGIA E SERVIÇOS LTDA' THEN '06'
        WHEN 'CONSORCIO CONSTRUTOR PR-323' THEN '07'
        WHEN 'CONSORCIO CONSTRUTOR CONSERVA-PR' THEN '08'
        WHEN 'VALE DAS PEDRAS' THEN '09'
        WHEN 'CONSORCIO RONCADOR' THEN '10'
        ELSE Z01.EMPRESA 
    END AS EMPRESA,
    Z01.Z01_FILIAL,
    Z01.Z01_CC,
    Z01.Z01_DECCC,
    CASE 
        WHEN CTT.CTT_BLOQ = '2' THEN 'ATIVO' ELSE 'BLOQUEADO' END AS 'CTT_BLOQ',
    SAL.AL_DESC,
    LPAD(SAL.AL_NIVEL, 2, '0') AS 'AL_NIVEL',
    SAK.AK_NOME,
    DHL.DHL_DESCRI,
    CASE 
        WHEN SAL.AL_TPLIBER = 'U' THEN 'USUARIO' WHEN SAL.AL_TPLIBER = 'N' THEN 'NIVEL' ELSE 'NENHUMA' END AS 'AL_TPLIBER',
    CASE 
        WHEN SAL.AL_MSBLQL = '2' THEN 'ATIVO' ELSE 'INATIVO' END AS 'AL_MSBLQL',
    Z01.Z01_PROJET AS 'GRUPO BLOQ?'
FROM Z01_CADASTRO_APROVADOR_CENTRO_CUSTO AS Z01
LEFT JOIN SAL_CADASTRO_GRUPO_APROVADORES AS SAL 
    ON SAL.EMPRESA = Z01.EMPRESA 
    AND SAL.AL_COD = Z01.Z01_GRAPRV 
    AND SAL.AL_FILIAL = Z01.Z01_FILIAL 
    AND SAL.AL_MSBLQL = '2'
LEFT JOIN SAK_CADASTRO_APROVADORES AS SAK 
    ON SAK.EMPRESA = Z01.EMPRESA 
    AND SAK.AK_COD = SAL.AL_APROV 
    AND SAK.AK_FILIAL = Z01.Z01_FILIAL
LEFT JOIN DHL_CADASTRO_PERFIL_APROVADORES AS DHL 
    ON DHL.EMPRESA = Z01.EMPRESA 
    AND DHL.DHL_COD = SAL.AL_PERFIL 
    AND DHL.DHL_FILIAL = Z01.Z01_FILIAL
LEFT JOIN CTT_CADASTRO_CENTRO_CUSTO AS CTT 
    ON CTT.EMPRESA = Z01.EMPRESA 
    AND CTT.CTT_CUSTO = Z01.Z01_CC 
    AND TRIM(CTT.CTT_FILIAL) = SUBSTRING(Z01.Z01_FILIAL, 1, 2) 
    AND CTT.D_E_L_E_T_ <> '*'
WHERE Z01.D_E_L_E_T_ <> '*'
    AND TRIM(Z01.Z01_FILIAL) IN (
        '010101', '010102', '020101', '030101', '030102', '040101', '050101', 
        '060101', '070101', '080101', '090101', '100101', 
        '110101', '120101', '130101'
    )
ORDER BY Z01.Z01_FILIAL ASC, Z01.Z01_CC ASC